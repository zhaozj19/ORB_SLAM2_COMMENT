ORBextractor::ORBextractor( int _nfeatures,         //每张图片提取的特征数量（整个金字塔所有的特征数）
                            float _scaleFactor,     //图片金字塔缩放因子
                            int _nlevels,           //图片金字塔层数
                            int _iniThFAST,         //图片被划分成网格，提取FAST关键点的时候的默认阈值
                            int _minThFAST):        //如果该网格按照默认阈值实在是找不出来太多特征点，则用较小的阈值
    nfeatures(_nfeatures), scaleFactor(_scaleFactor), nlevels(_nlevels),
    iniThFAST(_iniThFAST), minThFAST(_minThFAST)
{
    mvScaleFactor.resize(nlevels);
    mvLevelSigma2.resize(nlevels);
    mvScaleFactor[0]=1.0f;              //代表金字塔最下层的那张是原始的灰度图，不进行缩放。
    mvLevelSigma2[0]=1.0f;              //代表缩放因子的平方
    for(int i=1; i<nlevels; i++)
    {
        mvScaleFactor[i]=mvScaleFactor[i-1]*scaleFactor;
        mvLevelSigma2[i]=mvScaleFactor[i]*mvScaleFactor[i];
    }

    //mvInvScaleFactor存储的是
    mvInvScaleFactor.resize(nlevels);       // 调整容器的大小，使其包含n个元素。
    mvInvLevelSigma2.resize(nlevels);
    for(int i=0; i<nlevels; i++)
    {
        mvInvScaleFactor[i]=1.0f/mvScaleFactor[i];
        mvInvLevelSigma2[i]=1.0f/mvLevelSigma2[i];
    }

    mvImagePyramid.resize(nlevels);

    mnFeaturesPerLevel.resize(nlevels);
    float factor = 1.0f / scaleFactor;
    float nDesiredFeaturesPerScale = nfeatures*(1 - factor)/(1 - (float)pow((double)factor, (double)nlevels));

    int sumFeatures = 0;
    for( int level = 0; level < nlevels-1; level++ )
    {
        mnFeaturesPerLevel[level] = cvRound(nDesiredFeaturesPerScale);
        sumFeatures += mnFeaturesPerLevel[level];
        nDesiredFeaturesPerScale *= factor;
    }
    mnFeaturesPerLevel[nlevels-1] = std::max(nfeatures - sumFeatures, 0);

    //表示512个点，因为bit_pattern_31_数组定义的是256*4（也就是256*2*2，表示512个点，每个点由两个值）
    const int npoints = 512;
    //注意到pattern0数据类型为Points*,bit_pattern_31_是int[]型，所以这里需要进行强制类型转换
    const Point* pattern0 = (const Point*)bit_pattern_31_;
    //pattern指向512个离散点
    std::copy(pattern0, pattern0 + npoints, std::back_inserter(pattern));

    //This is for orientation
    // pre-compute the end of a row in a circular patch
    //umax保持最大个行数
    umax.resize(HALF_PATCH_SIZE + 1);

    //这下面的操作都是处理灰度质心法中的圆圈内每一行的像素数，结果保存在umax中
    //作者的思路看起来太难受，我不写那么详细了
    //cvFloor返回不大于参数的最大整数值（向下取整），cvCeil返回不小于参数的最小整数值（向上取整），cvRound则是四舍五入
    int v, v0, vmax = cvFloor(HALF_PATCH_SIZE * sqrt(2.f) / 2 + 1);
    int vmin = cvCeil(HALF_PATCH_SIZE * sqrt(2.f) / 2);
    const double hp2 = HALF_PATCH_SIZE*HALF_PATCH_SIZE;

    //这个用的是勾股定理
    for (v = 0; v <= vmax; ++v)
        umax[v] = cvRound(sqrt(hp2 - v * v));

    // Make sure we are symmetric
    for (v = HALF_PATCH_SIZE, v0 = 0; v >= vmin; --v)
    {
        while (umax[v0] == umax[v0 + 1])
            ++v0;
        umax[v] = v0;
        ++v0;
    }
}


//在四叉树中，父节点分裂为4个子节点的函数
void ExtractorNode::DivideNode(ExtractorNode &n1, ExtractorNode &n2, ExtractorNode &n3, ExtractorNode &n4)
{
    //得到当前节点的一半X，一半Y
    const int halfX = ceil(static_cast<float>(UR.x-UL.x)/2);
    const int halfY = ceil(static_cast<float>(BR.y-UL.y)/2);

    //Define boundaries of childs
    //定义子节点的边界
    n1.UL = UL;
    n1.UR = cv::Point2i(UL.x+halfX,UL.y);
    n1.BL = cv::Point2i(UL.x,UL.y+halfY);
    n1.BR = cv::Point2i(UL.x+halfX,UL.y+halfY);
    n1.vKeys.reserve(vKeys.size());     //默认子节点的特征数和父节点一样多（这里只是预分配内存，并不存在初始化的操作）

    n2.UL = n1.UR;
    n2.UR = UR;
    n2.BL = n1.BR;
    n2.BR = cv::Point2i(UR.x,UL.y+halfY);
    n2.vKeys.reserve(vKeys.size());

    n3.UL = n1.BL;
    n3.UR = n1.BR;
    n3.BL = BL;
    n3.BR = cv::Point2i(n1.BR.x,BL.y);
    n3.vKeys.reserve(vKeys.size());

    n4.UL = n3.UR;
    n4.UR = n2.BR;
    n4.BL = n3.BR;
    n4.BR = BR;
    n4.vKeys.reserve(vKeys.size());

    //Associate points to childs
    //通过计算特征点位于哪个子节点中，把父节点的特征点划分给子节点
    for(size_t i=0;i<vKeys.size();i++)
    {
        const cv::KeyPoint &kp = vKeys[i];
        if(kp.pt.x<n1.UR.x)
        {
            if(kp.pt.y<n1.BR.y)
                n1.vKeys.push_back(kp);
            else
                n3.vKeys.push_back(kp);
        }
        else if(kp.pt.y<n1.BR.y)
            n2.vKeys.push_back(kp);
        else
            n4.vKeys.push_back(kp);
    }

    //默认子节点都可以再分裂
    if(n1.vKeys.size()==1)
        n1.bNoMore = true;
    if(n2.vKeys.size()==1)
        n2.bNoMore = true;
    if(n3.vKeys.size()==1)
        n3.bNoMore = true;
    if(n4.vKeys.size()==1)
        n4.bNoMore = true;

}

vector<cv::KeyPoint> ORBextractor::DistributeOctTree(   const vector<cv::KeyPoint>& vToDistributeKeys,  //金字塔这一层图片中的特征点
                                                        const int &minX,        //提取特征区域的最小X
                                                        const int &maxX,        //提取特征区域的最大X
                                                        const int &minY,        //提取特征区域的最小Y
                                                        const int &maxY,        //提取特征区域的最大Y
                                                        const int &N,           //这一层应该提取多少个特征点
                                                        const int &level)       //金字塔的本层数
{
    // Compute how many initial nodes   
    //计算初始节点个数
    //因为如果图片较宽，用w/h的方式，比如说一张640x480的图片，此时就一个节点
    const int nIni = round(static_cast<float>(maxX-minX)/(maxY-minY));

    //计算节点x的范围
    const float hX = static_cast<float>(maxX-minX)/nIni;

    //代表四叉树中所有有效的节点（因为在下面分裂的过程中，如果某一个节点的特征数为0就会在链表中抹去）
    list<ExtractorNode> lNodes;
    //存储节点四叉树中节点的指针（定义这个变量是为了把vToDistributeKeys中的特征点都分配到初始节点中）
    vector<ExtractorNode*> vpIniNodes;
    vpIniNodes.resize(nIni);

    //生成nIni个节点，结果全部保存在lNodes中，这个lNodes就是四叉树本身了，特别重要！！！
    for(int i=0; i<nIni; i++)
    {
        ExtractorNode ni;
        ni.UL = cv::Point2i(hX*static_cast<float>(i),0);
        ni.UR = cv::Point2i(hX*static_cast<float>(i+1),0);
        ni.BL = cv::Point2i(ni.UL.x,maxY-minY);
        ni.BR = cv::Point2i(ni.UR.x,maxY-minY);
        ni.vKeys.reserve(vToDistributeKeys.size());

        lNodes.push_back(ni);
        //通过back函数提取出新加入的节点，把其指针赋值给vpIniNodes
        vpIniNodes[i] = &lNodes.back();
    }

    //Associate points to childs
    //把vToDistributeKeys中的特征点都分配到初始节点中（通过特征点的x坐标除以hX就可以的到节点的index，就可以push了）
    for(size_t i=0;i<vToDistributeKeys.size();i++)
    {
        const cv::KeyPoint &kp = vToDistributeKeys[i];
        vpIniNodes[kp.pt.x/hX]->vKeys.push_back(kp);
    }

    //接下来就开始分裂了，一切围绕着lit迭代器。
    list<ExtractorNode>::iterator lit = lNodes.begin();


    //这里还是先对初始节点进行处理，把特征点数为1的节点，设置为不可分裂状态
    //再把没有特征点的节点直接抹去
    while(lit!=lNodes.end())
    {
        if(lit->vKeys.size()==1)
        {
            lit->bNoMore=true;
            lit++;
        }
        else if(lit->vKeys.empty())
            lit = lNodes.erase(lit);
        else
            lit++;
    }


    //迭代是否结束的标志
    bool bFinish = false;
    //迭代次数
    int iteration = 0;

    vector<pair<int,ExtractorNode*> > vSizeAndPointerToNode;
    vSizeAndPointerToNode.reserve(lNodes.size()*4);

    while(!bFinish)
    {
        iteration++;

        int prevSize = lNodes.size();

        lit = lNodes.begin();


        //在分裂过程中，计算某一轮分裂中，在分裂出来的子节点中，还可以继续分裂（子节点特征数大于1）的子节点数量
        int nToExpand = 0;

        vSizeAndPointerToNode.clear();

        while(lit!=lNodes.end())
        {
            if(lit->bNoMore)
            {
                // If node only contains one point do not subdivide and continue
                //判断当前节点是否处于不可分裂状态
                lit++;
                continue;
            }
            else
            {
                // If more than one point, subdivide
                //父节点分裂为4个子节点的操作
                ExtractorNode n1,n2,n3,n4;      //首先定义4个子节点
                lit->DivideNode(n1,n2,n3,n4);   //lit代表当亲节点的指针，调用DivideNode函数进行分裂

                // Add childs if they contain points
                if(n1.vKeys.size()>0)
                {
                    lNodes.push_front(n1);      //把新节点插入list头部
                    if(n1.vKeys.size()>1)
                    {
                        nToExpand++;        //可分裂的节点数+1
                        
                        //把n1节点的特征点数和指针保存下来
                        vSizeAndPointerToNode.push_back(make_pair(n1.vKeys.size(),&lNodes.front()));
                        //把n1节点的lit赋值（其实就是本身）
                        lNodes.front().lit = lNodes.begin();
                    }
                }
                if(n2.vKeys.size()>0)
                {
                    lNodes.push_front(n2);
                    if(n2.vKeys.size()>1)
                    {
                        nToExpand++;
                        vSizeAndPointerToNode.push_back(make_pair(n2.vKeys.size(),&lNodes.front()));
                        lNodes.front().lit = lNodes.begin();
                    }
                }
                if(n3.vKeys.size()>0)
                {
                    lNodes.push_front(n3);
                    if(n3.vKeys.size()>1)
                    {
                        nToExpand++;
                        vSizeAndPointerToNode.push_back(make_pair(n3.vKeys.size(),&lNodes.front()));
                        lNodes.front().lit = lNodes.begin();
                    }
                }
                if(n4.vKeys.size()>0)
                {
                    lNodes.push_front(n4);
                    if(n4.vKeys.size()>1)
                    {
                        nToExpand++;
                        vSizeAndPointerToNode.push_back(make_pair(n4.vKeys.size(),&lNodes.front()));
                        lNodes.front().lit = lNodes.begin();
                    }
                }

                lit=lNodes.erase(lit);      //把已经分裂的父节点抹去，并返回下一个节点
                continue;
            }
        }       

        // Finish if there are more nodes than required features
        // or all nodes contain just one point
        //停止节点分裂的过程需要满足下面两个条件之一：
        //1.当前四叉树中的节点数已经超过了金字塔这一层中所需要的特征点数（N）
        //2.当前四叉树中的所有节点中，都只有一个特征点（这里作者用一个变量prevSize来判断lNodes.size是否发生了变化，很聪明）
        if((int)lNodes.size()>=N || (int)lNodes.size()==prevSize)
        {
            bFinish = true;
        }
        //如果当前的lNodes中的节点数，再加上其中可以分裂的节点数的三倍大于N，就让那些可以继续分裂的节点，接着分裂
        //这里的3，是因为节点虽然分裂出了4个，但是自己本身却被抹去了，而lNodes.size()已经包含了可以分裂的节点，所以只需要加3
        //疑问：为什么要单独搞一个 else if来分裂节点呐，直接在返回到上面的while里继续分裂不就好了吗？
        else if(((int)lNodes.size()+nToExpand*3)>N)
        {

            while(!bFinish)
            {

                prevSize = lNodes.size();

                vector<pair<int,ExtractorNode*> > vPrevSizeAndPointerToNode = vSizeAndPointerToNode;
                vSizeAndPointerToNode.clear();

                //这里按照节点内的特征点数，从小到大进行排列
                //然后for循环倒着来，优先分裂那些节点中密集的特征点的节点
                //可以起到让特征点多的区域先分裂，防止特征点扎堆，也可以早日满足特征点达到N的要求，早早退出循环
                sort(vPrevSizeAndPointerToNode.begin(),vPrevSizeAndPointerToNode.end());
                for(int j=vPrevSizeAndPointerToNode.size()-1;j>=0;j--)
                {
                    ExtractorNode n1,n2,n3,n4;
                    vPrevSizeAndPointerToNode[j].second->DivideNode(n1,n2,n3,n4);

                    // Add childs if they contain points
                    if(n1.vKeys.size()>0)
                    {
                        lNodes.push_front(n1);
                        if(n1.vKeys.size()>1)
                        {
                            vSizeAndPointerToNode.push_back(make_pair(n1.vKeys.size(),&lNodes.front()));
                            lNodes.front().lit = lNodes.begin();
                        }
                    }
                    if(n2.vKeys.size()>0)
                    {
                        lNodes.push_front(n2);
                        if(n2.vKeys.size()>1)
                        {
                            vSizeAndPointerToNode.push_back(make_pair(n2.vKeys.size(),&lNodes.front()));
                            lNodes.front().lit = lNodes.begin();
                        }
                    }
                    if(n3.vKeys.size()>0)
                    {
                        lNodes.push_front(n3);
                        if(n3.vKeys.size()>1)
                        {
                            vSizeAndPointerToNode.push_back(make_pair(n3.vKeys.size(),&lNodes.front()));
                            lNodes.front().lit = lNodes.begin();
                        }
                    }
                    if(n4.vKeys.size()>0)
                    {
                        lNodes.push_front(n4);
                        if(n4.vKeys.size()>1)
                        {
                            vSizeAndPointerToNode.push_back(make_pair(n4.vKeys.size(),&lNodes.front()));
                            lNodes.front().lit = lNodes.begin();
                        }
                    }


                    //分裂完毕之后，就删除本节点。上面的lNodes.front().lit = lNodes.begin();原来是为了利用lit删除自己用的
                    lNodes.erase(vPrevSizeAndPointerToNode[j].second->lit);

                    if((int)lNodes.size()>=N)
                        break;
                }

                //解释同上
                if((int)lNodes.size()>=N || (int)lNodes.size()==prevSize)
                    bFinish = true;

            }
        }
    }

    // Retain the best point in each node
    //这时候已经退出了上面的分裂过程，
    vector<cv::KeyPoint> vResultKeys;
    vResultKeys.reserve(nfeatures);
    for(list<ExtractorNode>::iterator lit=lNodes.begin(); lit!=lNodes.end(); lit++)
    {
        vector<cv::KeyPoint> &vNodeKeys = lit->vKeys;
        cv::KeyPoint* pKP = &vNodeKeys[0];
        //KeyPoint这数据结构中有如下数据成员
        //CV_WRAP KeyPoint() : pt(0,0), size(0), angle(-1), response(0), octave(0), class_id(-1) {}
        //pt(x,y):关键点的点坐标；
        //size():该关键点邻域直径大小；
        //angle:角度，表示关键点的方向，值为[零,三百六十)，负值表示不使用。
        //response:响应强度，代表该点强壮大小，即该点是特征点的程度。
        //octave:从哪一层金字塔得到的此关键点。
        //class_id:当要对图片进行分类时，用class_id对每个关键点进行区分，默认为-1。
        //我们要找出一个节点中，最健壮的特征点，就是根据response属性来找的
        float maxResponse = pKP->response;


        //这个for循环，貌似很简单，都能看得懂
        //用size_t来定义k，很好的习惯
        for(size_t k=1;k<vNodeKeys.size();k++)
        {
            if(vNodeKeys[k].response>maxResponse)
            {
                pKP = &vNodeKeys[k];
                maxResponse = vNodeKeys[k].response;
            }
        }

        vResultKeys.push_back(*pKP);
    }
    
    //看到这里，就知道了为什么作者在while循环里面做了那么多小技巧
    //全都是为了让提取出来的特征点分散，包括对可分裂节点按照特征点数量从小到大排序
    //然后返回vResultKeys，就得到了金字塔这一层所需要的特征点数（注意啊，这里得到的特征点有可能是大于N的）
    return vResultKeys;
}


//使用八叉树的方式提取图片金字塔中每层图片的特征点
void ORBextractor::ComputeKeyPointsOctTree(vector<vector<KeyPoint> >& allKeypoints)
{
    //resize成金字塔的大小
    allKeypoints.resize(nlevels);

    //之前提到过，ORB-SLAM2会将图片分成多干个grid，每个grid的大小就是30*30
    const float W = 30;

    for (int level = 0; level < nlevels; ++level)
    {
        //这4个变量表示这层图片的坐标边界，这里的3是因为在计算FAST特征点的时候，需要建立一个半径为3的圆
        const int minBorderX = EDGE_THRESHOLD-3;
        const int minBorderY = minBorderX;
        const int maxBorderX = mvImagePyramid[level].cols-EDGE_THRESHOLD+3;
        const int maxBorderY = mvImagePyramid[level].rows-EDGE_THRESHOLD+3;

        //vToDistributeKeys存储这一层需要分配的特征点；
        //为什么需要分配呐？（待更新）
        //因为一般采集的特征点都很多，这里预先乘以10，后续还会处理
        //这里注意一下resize和reserve的区别：resize为vector分配指定大小内存并且初始化，reserve也为vector分配内存，但不及逆行初始化。
        vector<cv::KeyPoint> vToDistributeKeys;
        vToDistributeKeys.reserve(nfeatures*10);

        //待提取图片区域的宽和高
        const float width = (maxBorderX-minBorderX);
        const float height = (maxBorderY-minBorderY);

        //标记当前网格所在图片的下表（比如一个图片分成10*10个网格，那么有一个网格的坐标为（1，2），这两个值就是标记这个的）
        const int nCols = width/W;
        const int nRows = height/W;
        //标记网格内的像素宽高
        const int wCell = ceil(width/nCols);
        const int hCell = ceil(height/nRows);

        for(int i=0; i<nRows; i++)
        {
            //计算当前行的初始Y坐标以及最大Y坐标
            //为了更好的提取FAST关键点，这里把maxY多设置了6个像素（3+3）
            const float iniY =minBorderY+i*hCell;
            float maxY = iniY+hCell+6;

            if(iniY>=maxBorderY-3)
                continue;
            if(maxY>maxBorderY)
                maxY = maxBorderY;

            for(int j=0; j<nCols; j++)
            {
                const float iniX =minBorderX+j*wCell;
                float maxX = iniX+wCell+6;
                if(iniX>=maxBorderX-6)
                    continue;
                if(maxX>maxBorderX)
                    maxX = maxBorderX;

                //每个cell里面的FAST关键点
                vector<cv::KeyPoint> vKeysCell;

                //调用OpenCV的库函数来提取FAST关键点
                //void FAST(InputArray image, vector<KeyPoint>& keypoints, int threshold, bool nonmaxSuppression=true)
                //image – 输入灰度图像。
                //keypoints – 检测到的特征点。
                //threshold – 中心像素的像素值和该像素周围的像素值之差的阈值。
                //nonmaxSuppression – 是否对特征点采用极大值抑制。
                FAST(mvImagePyramid[level].rowRange(iniY,maxY).colRange(iniX,maxX),
                     vKeysCell,iniThFAST,true);

                if(vKeysCell.empty())
                {
                    //如果按照默认的阈值，找不到特征点，则改用较小的阈值
                    //如果还找不到呐？那就接着用更小的阈值吧。
                    FAST(mvImagePyramid[level].rowRange(iniY,maxY).colRange(iniX,maxX),
                         vKeysCell,minThFAST,true);
                }

                if(!vKeysCell.empty())
                {
                    ////遍历其中的所有FAST角点（注意这里用的是vector<cv::KeyPoint>提供的迭代器）
                    //现在我们需要把cell里面特征点的坐标转换成在图片中的坐标，这是为了下面构造八叉树做准备
                    for(vector<cv::KeyPoint>::iterator vit=vKeysCell.begin(); vit!=vKeysCell.end();vit++)
                    {
                        (*vit).pt.x+=j*wCell;
                        (*vit).pt.y+=i*hCell;
                        vToDistributeKeys.push_back(*vit);
                    }
                }

            }
        }

        //声明一个对当前图层的特征点的容器的引用
        //这里通过用allKeypoints初始化引用容器的方法，把allKeypoints和金字塔特征点联系起来
        vector<KeyPoint> & keypoints = allKeypoints[level];
        //预申请大小，默认值为1000
        keypoints.reserve(nfeatures);

        //由于在cell中提取的关键点在很大程度上是扎堆出现的，因此需要极大值抑制
        //这个操作是通过将将当前所提取出来的特征点分配到平面四叉树（就是四叉树啊，怎么那么多文献说八叉树）中实现的
        keypoints = DistributeOctTree(vToDistributeKeys, minBorderX, maxBorderX,
                                      minBorderY, maxBorderY,mnFeaturesPerLevel[level], level);

        //scaledPatchSize用来记录关键点邻域直径大小，用来计算特征点的方向信息的
        //PATCH_SIZE是按照金字塔底层图像（也就是原图）所涉及的灰度质心法中的圆圈直径，所以金字塔的上层也要按照缩放比例对这个PATCH_SIZE进行缩放
        const int scaledPatchSize = PATCH_SIZE*mvScaleFactor[level];

        // Add border to coordinates and scale information
        //nkps代表这一层图像所提取出来的特征点数
        const int nkps = keypoints.size();
        for(int i=0; i<nkps ; i++)
        {
            //对每一个保留下来的特征点，恢复到相对于当前图层“边缘扩充图像下”的坐标系的坐标
            keypoints[i].pt.x+=minBorderX;
            keypoints[i].pt.y+=minBorderY;
            //记录特征点来源的图像金字塔图层
            keypoints[i].octave=level;
            //记录计算方向的patch，缩放后对应的大小， 又被称作为特征点半径
            keypoints[i].size = scaledPatchSize;
        }
    }

    // compute orientations
    for (int level = 0; level < nlevels; ++level)
    {
        //计算特征点的方向信息
        //mvImagePyramid[level]保存的是金字塔中每一层的灰度图
        //allKeypoints[level]保存的是金字塔中每一层的特征点
        //umax代表PATCH的横坐标边界
        computeOrientation(mvImagePyramid[level], allKeypoints[level], umax);
    }
        
}

void ORBextractor::ComputeKeyPointsOld(std::vector<std::vector<KeyPoint> > &allKeypoints)
{
    allKeypoints.resize(nlevels);

    float imageRatio = (float)mvImagePyramid[0].cols/mvImagePyramid[0].rows;

    for (int level = 0; level < nlevels; ++level)
    {
        const int nDesiredFeatures = mnFeaturesPerLevel[level];

        const int levelCols = sqrt((float)nDesiredFeatures/(5*imageRatio));
        const int levelRows = imageRatio*levelCols;

        const int minBorderX = EDGE_THRESHOLD;
        const int minBorderY = minBorderX;
        const int maxBorderX = mvImagePyramid[level].cols-EDGE_THRESHOLD;
        const int maxBorderY = mvImagePyramid[level].rows-EDGE_THRESHOLD;

        const int W = maxBorderX - minBorderX;
        const int H = maxBorderY - minBorderY;
        const int cellW = ceil((float)W/levelCols);
        const int cellH = ceil((float)H/levelRows);

        const int nCells = levelRows*levelCols;
        const int nfeaturesCell = ceil((float)nDesiredFeatures/nCells);

        vector<vector<vector<KeyPoint> > > cellKeyPoints(levelRows, vector<vector<KeyPoint> >(levelCols));

        vector<vector<int> > nToRetain(levelRows,vector<int>(levelCols,0));
        vector<vector<int> > nTotal(levelRows,vector<int>(levelCols,0));
        vector<vector<bool> > bNoMore(levelRows,vector<bool>(levelCols,false));
        vector<int> iniXCol(levelCols);
        vector<int> iniYRow(levelRows);
        int nNoMore = 0;
        int nToDistribute = 0;


        float hY = cellH + 6;

        for(int i=0; i<levelRows; i++)
        {
            const float iniY = minBorderY + i*cellH - 3;
            iniYRow[i] = iniY;

            if(i == levelRows-1)
            {
                hY = maxBorderY+3-iniY;
                if(hY<=0)
                    continue;
            }

            float hX = cellW + 6;

            for(int j=0; j<levelCols; j++)
            {
                float iniX;

                if(i==0)
                {
                    iniX = minBorderX + j*cellW - 3;
                    iniXCol[j] = iniX;
                }
                else
                {
                    iniX = iniXCol[j];
                }


                if(j == levelCols-1)
                {
                    hX = maxBorderX+3-iniX;
                    if(hX<=0)
                        continue;
                }


                Mat cellImage = mvImagePyramid[level].rowRange(iniY,iniY+hY).colRange(iniX,iniX+hX);

                cellKeyPoints[i][j].reserve(nfeaturesCell*5);

                FAST(cellImage,cellKeyPoints[i][j],iniThFAST,true);

                if(cellKeyPoints[i][j].size()<=3)
                {
                    cellKeyPoints[i][j].clear();

                    FAST(cellImage,cellKeyPoints[i][j],minThFAST,true);
                }


                const int nKeys = cellKeyPoints[i][j].size();
                nTotal[i][j] = nKeys;

                if(nKeys>nfeaturesCell)
                {
                    nToRetain[i][j] = nfeaturesCell;
                    bNoMore[i][j] = false;
                }
                else
                {
                    nToRetain[i][j] = nKeys;
                    nToDistribute += nfeaturesCell-nKeys;
                    bNoMore[i][j] = true;
                    nNoMore++;
                }

            }
        }


        // Retain by score

        while(nToDistribute>0 && nNoMore<nCells)
        {
            int nNewFeaturesCell = nfeaturesCell + ceil((float)nToDistribute/(nCells-nNoMore));
            nToDistribute = 0;

            for(int i=0; i<levelRows; i++)
            {
                for(int j=0; j<levelCols; j++)
                {
                    if(!bNoMore[i][j])
                    {
                        if(nTotal[i][j]>nNewFeaturesCell)
                        {
                            nToRetain[i][j] = nNewFeaturesCell;
                            bNoMore[i][j] = false;
                        }
                        else
                        {
                            nToRetain[i][j] = nTotal[i][j];
                            nToDistribute += nNewFeaturesCell-nTotal[i][j];
                            bNoMore[i][j] = true;
                            nNoMore++;
                        }
                    }
                }
            }
        }

        vector<KeyPoint> & keypoints = allKeypoints[level];
        keypoints.reserve(nDesiredFeatures*2);

        const int scaledPatchSize = PATCH_SIZE*mvScaleFactor[level];

        // Retain by score and transform coordinates
        for(int i=0; i<levelRows; i++)
        {
            for(int j=0; j<levelCols; j++)
            {
                vector<KeyPoint> &keysCell = cellKeyPoints[i][j];
                KeyPointsFilter::retainBest(keysCell,nToRetain[i][j]);
                if((int)keysCell.size()>nToRetain[i][j])
                    keysCell.resize(nToRetain[i][j]);


                for(size_t k=0, kend=keysCell.size(); k<kend; k++)
                {
                    keysCell[k].pt.x+=iniXCol[j];
                    keysCell[k].pt.y+=iniYRow[i];
                    keysCell[k].octave=level;
                    keysCell[k].size = scaledPatchSize;
                    keypoints.push_back(keysCell[k]);
                }
            }
        }

        if((int)keypoints.size()>nDesiredFeatures)
        {
            KeyPointsFilter::retainBest(keypoints,nDesiredFeatures);
            keypoints.resize(nDesiredFeatures);
        }
    }

    // and compute orientations
    for (int level = 0; level < nlevels; ++level)
        computeOrientation(mvImagePyramid[level], allKeypoints[level], umax);
}

static void computeDescriptors(const Mat& image, vector<KeyPoint>& keypoints, Mat& descriptors,
                               const vector<Point>& pattern)
{
    descriptors = Mat::zeros((int)keypoints.size(), 32, CV_8UC1);

    for (size_t i = 0; i < keypoints.size(); i++)
        computeOrbDescriptor(keypoints[i], image, &pattern[0], descriptors.ptr((int)i));
}

//完成对灰度图的ORB特征提取
void ORBextractor::operator()(  InputArray _image,              //灰度图；InputArray和OutputArray都是OpenCV里的代理类，前者默认加了const限定符，后者则可变
                                InputArray _mask,               //掩膜（常用于提取灰度图兴趣区等操作，这里这个变量没有用到）
                                vector<KeyPoint>& _keypoints,   //保存灰度图的关键点
                      OutputArray _descriptors)                 //保存灰度图的描述子，为矩阵类型
{ 
    if(_image.empty())
        return;

    Mat image = _image.getMat();        //从输入的矩阵数组中提取矩阵；getMat(int idx = -1)默认参数为-1
    assert(image.type() == CV_8UC1 );   //CV_8UC1 是指一个8位无符号整型单通道矩阵，S = 符号整型 U = 无符号整型 F = 浮点型

    // Pre-compute the scale pyramid
    //首先构造图像金字塔，详情参见ComputePyramid函数
    ComputePyramid(image);


    //allKeypoints存储图片金字塔的ORB特征点
    vector < vector<KeyPoint> > allKeypoints;
    //使用八叉树的方式提取每层图片的特征点
    ComputeKeyPointsOctTree(allKeypoints);
    //ComputeKeyPointsOld(allKeypoints);

    Mat descriptors;

    int nkeypoints = 0;
    for (int level = 0; level < nlevels; ++level)
        nkeypoints += (int)allKeypoints[level].size();
    if( nkeypoints == 0 )
        _descriptors.release();
    else
    {
        _descriptors.create(nkeypoints, 32, CV_8U);
        descriptors = _descriptors.getMat();
    }

    _keypoints.clear();
    _keypoints.reserve(nkeypoints);

    int offset = 0;
    for (int level = 0; level < nlevels; ++level)
    {
        vector<KeyPoint>& keypoints = allKeypoints[level];
        int nkeypointsLevel = (int)keypoints.size();

        if(nkeypointsLevel==0)
            continue;

        // preprocess the resized image
        Mat workingMat = mvImagePyramid[level].clone();
        GaussianBlur(workingMat, workingMat, Size(7, 7), 2, 2, BORDER_REFLECT_101);

        // Compute the descriptors
        Mat desc = descriptors.rowRange(offset, offset + nkeypointsLevel);
        computeDescriptors(workingMat, keypoints, desc, pattern);

        offset += nkeypointsLevel;

        // Scale keypoint coordinates
        if (level != 0)
        {
            float scale = mvScaleFactor[level]; //getScale(level, firstLevel, scaleFactor);
            for (vector<KeyPoint>::iterator keypoint = keypoints.begin(),
                 keypointEnd = keypoints.end(); keypoint != keypointEnd; ++keypoint)
                keypoint->pt *= scale;
        }
        // And add the keypoints to the output
        _keypoints.insert(_keypoints.end(), keypoints.begin(), keypoints.end());
    }
}

//构造图像金字塔
void ORBextractor::ComputePyramid(cv::Mat image)
{
    for (int level = 0; level < nlevels; ++level)
    {
        //首先获取金字塔每一层的缩放率（从低到高，最底层是1）
        float scale = mvInvScaleFactor[level];
        //cvRound 返回跟参数最接近的整数值；cvFloor 返回不大于参数的最大整数值；cvCeil 返回不小于参数的最小整数值。
        //计算本层缩放后图片的尺寸大小
        Size sz(cvRound((float)image.cols*scale), cvRound((float)image.rows*scale));
        //在缩放后的尺寸基础上，又另外加了EDGE_THRESHOLD*2的尺寸，这么加的作用可能是为了让缩放后的边缘像素，也可以有效的提取ORB特征。
        Size wholeSize(sz.width + EDGE_THRESHOLD*2, sz.height + EDGE_THRESHOLD*2);
        //生成temp和masktemp矩阵，第一个变量用来生成缩放之后的灰度图，第二个没用到。
        Mat temp(wholeSize, image.type()), masktemp;
        mvImagePyramid[level] = temp(Rect(EDGE_THRESHOLD, EDGE_THRESHOLD, sz.width, sz.height));

        // Compute the resized image
        if( level != 0 )
        {
            //resize函数来改变图像的大小
            //void resize(InputArray src, OutputArray dst, Size dsize, double fx=0, double fy=0, int interpolation=INTER_LINEAR );
            //src：输入，原图像，即待改变大小的图像；
            //dst：输出，改变大小之后的图像，这个图像和原图像具有相同的内容，只是大小和原图像不一样而已；
            //dsize：输出图像的大小。
            //fx：width方向的缩放比例
            //fy：height方向的缩放比例
            //interpolation：这个是指定插值的方式，图像缩放之后，肯定像素要进行重新计算的，就靠这个参数来指定重新计算像素的方式
            //此处为INTER_LINEAR - 双线性插值，如果最后一个参数你不指定，默认使用这种方法
            resize(mvImagePyramid[level-1], mvImagePyramid[level], sz, 0, 0, INTER_LINEAR);

            copyMakeBorder(mvImagePyramid[level], temp, EDGE_THRESHOLD, EDGE_THRESHOLD, EDGE_THRESHOLD, EDGE_THRESHOLD,
                           BORDER_REFLECT_101+BORDER_ISOLATED);            
        }
        else
        {
            //copyMakeBorder()函数可以复制图像并制作边界，将特定图像轻微变大，然后以各种方式自动填充图
            //void copyMakeBorder(InputArray  src,   输入图像
		                    //  OutputArray dst,     输出图像
							//  int top,             表示示对边界每个方向添加的像素个数，边界的方向包括上下左右
							//  int bottom,          
							//  int left,            
							//  int right, 
							//  int borderType,       表示边界的类型，BORDER_REFLECT_101（gfedcb|abcdefgh|gfedcba 以边界为对称轴反射复制像素）

            copyMakeBorder(image, temp, EDGE_THRESHOLD, EDGE_THRESHOLD, EDGE_THRESHOLD, EDGE_THRESHOLD,
                           BORDER_REFLECT_101);            
        }
    }

}

